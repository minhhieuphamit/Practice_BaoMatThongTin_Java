    private void jButtonKhoaChungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonKhoaChungActionPerformed
        // TODO add your handling code here:
        try {
            FileInputStream fis = new FileInputStream("H:\\BaoMatThongTin\\TH_BMTT\\Tuan4\\B.pub");
            byte[] bobPubKeyEnc = new byte[fis.available()];
            fis.read(bobPubKeyEnc);
            fis.close();

            KeyFactory aliceKeyFac = KeyFactory.getInstance("DH");
            X509EncodedKeySpec x509KeySpec = new X509EncodedKeySpec(bobPubKeyEnc);
            bobPubKey = aliceKeyFac.generatePublic(x509KeySpec);
            System.out.println("Excuting PHASE1 of key argreement");
            aliceKeyAgree.doPhase(bobPubKey, true);
            byte[] aliceSharedSecret = aliceKeyAgree.generateSecret();

            System.out.println("Khoa chung: secret (DEBUG ONLY)" + CryptoUtil.toHexString(aliceSharedSecret));
            jTextFieldKhoaKAB.setText(CryptoUtil.toHexString(aliceSharedSecret));
        } catch (Exception ex) {

        }
    }//GEN-LAST:event_jButtonKhoaChungActionPerformed

